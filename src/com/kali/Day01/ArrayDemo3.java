public class ArrayDemo3 {
    public static void main(String[] args) {
        // 定义一个数组
        int[] arr = {5, 6, 7, 8, 9};
        // 通过for循环访问数组元素
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }

        // 快速生成数组的循环：数组名.fori
    }
}
